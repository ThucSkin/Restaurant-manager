    private void btnDangNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangNhapActionPerformed
        // TODO add your handling code here:
        txtMaNV.setBackground(white);
        txtPass.setBackground(white);
        if(txtMaNV.getText().trim().length()>0){
            if(txtPass.getPassword().length>0){
                dangNhap();
            }else{
                txtPass.setBackground(cyan);
                Auth.alert(this, "Không được để trống tên mật khẩu");
            }
        }else{
            txtMaNV.setBackground(cyan);
            Auth.alert(this, "Không được để trống tên đăng nhập");
        }
    }//GEN-LAST:event_btnDangNhapActionPerformed

