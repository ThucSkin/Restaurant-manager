    void dangNhap(){
        String strMaNV = txtMaNV.getText();
        String strPassword = new String(txtPass.getPassword());
        NV nv = dao.selectById(strMaNV);
        if(nv == null){
            MsgBox.alert(this, "Sai tên đăng nhập hoặc tài khoản không tồn tại!");
        }else{
            if(!nv.getMatKhau().equals(strPassword)){
                MsgBox.alert(this, "mật khẩu không đúng!");
            }else{
                Auth.user = nv;
                this.dispose();
            }
        }
    }
